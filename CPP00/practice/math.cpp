#include <iostream>
#include <cmath>

int main()
{
    std::cout << "max of 5 and 10 is: " << std::max(5, 10) << std::endl;
    std::cout << "min of 5 and 10 is: " << std::min(5, 10) << std::endl;
    std::cout << "sqrt of 64 is: " << sqrt(64) << std::endl;
    std::cout << "round of 2.6 is: " << round(2.4) << std::endl;
    std::cout << "log of 2 is: " << log(2) << std::endl;
    return (0);
}